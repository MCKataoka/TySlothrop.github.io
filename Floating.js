var bar1 = document.getElementById("bar1");
//add the class that makes the bars float when the mouse hovers over the bars
bar1.addEventListener(
  "mouseover",
  function() {
    bar1.classList.add("floating");

    setTimeout(function() {
      bar1.classList.remove("floating");
    }, 12000);
  },
  false
);

